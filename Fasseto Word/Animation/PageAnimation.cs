

namespace Fasseto_Word
{
    /// <summary>
    /// Style of page animation slidIn/slidOut
    /// </summary>
    public enum PageAnimation
    {
        /// <summary>
        /// No animation takes place
        /// </summary>
        None = 0,
        /// <summary>
        /// The page slid in and fade in from the right
        /// </summary>
        SlidAndFadeInFromRight = 1,
        /// <summary>
        /// The page slid out and fade out to the left
        /// </summary>
        SlidAndFadeOutToLeft = 2

    }
}
